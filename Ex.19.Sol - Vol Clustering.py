# %% IMPORT LIBRARIES AND SETUP

# Import the pandas library for data manipulation and analysis
import pandas as pd
# Import the numpy library for numerical operations and array handling
import numpy as np
# Import the pyplot module from matplotlib for creating visualizations
import matplotlib.pyplot as plt
# Import yfinance to download daily financial data directly from Yahoo Finance
import yfinance as yf
# Import the main statsmodels API for statistical modeling
import statsmodels.api as sm
# Import AutoReg for autoregressive modeling and ar_select_order for lag selection
from statsmodels.tsa.ar_model import AutoReg, ar_select_order
# Import the warnings module to handle warning messages
import warnings

# Suppress all warnings to keep the console output clean
warnings.filterwarnings("ignore")

# Print a separator line and a header for the data downloading section
print("="*50)
print("DATA COLLECTION: DAX 2022")
print("="*50)

# Define the ticker symbol for the DAX index on Yahoo Finance
ticker = "^GDAXI"
# Define the start and end dates for the year 2022
start_date = "2022-01-01"
# We use Jan 1st 2023 as end date to ensure we capture all days of December 2022
end_date = "2023-01-01"

print(f"Downloading daily data for {ticker}...")
# Download the historical market data using yfinance
df = yf.download(ticker, start=start_date, end=end_date, progress=False)

# Calculate the daily log returns using the Close price
df['return'] = np.log(df['Close']).diff()

# Calculate the squared daily returns (which serve as a proxy for daily variance/volatility)
df['sq_return'] = df['return'] ** 2

# Drop any missing values generated by the differencing
df = df.dropna()

# Print the first few rows to verify the data processing
print("\nData Head:")
print(df[['Close', 'return', 'sq_return']].head(3))


# %% 1. VISUAL ANALYSIS

# Print a separator line for the visual analysis section
print("\n" + "="*50)
# Print the title for the visual analysis
print("1. VISUAL ANALYSIS: RETURNS VS SQUARED RETURNS")
# Print the closing separator line
print("="*50)

# Initialize a new figure with two subplots stacked vertically
plt.figure(figsize=(12, 8))

# Create the top subplot for the standard returns
plt.subplot(2, 1, 1)
