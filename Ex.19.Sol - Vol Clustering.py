# %% 2. MODELING SQUARED RETURNS (VOLATILITY DYNAMICS)

# Print a separator line for the squared returns modeling section
print("\n" + "="*50)
# Print the title for the squared returns analysis
print("2. AR(p) MODEL FOR SQUARED RETURNS")
# Print the closing separator line
print("="*50)

# Set the maximum number of lags to consider
max_lag_test = 10

# Manually compute and print the AIC for each lag to open the "black box" of model selection.
# CRITICAL: We use 'hold_back=max_lag_test' to force all models to be estimated on the 
# exact same sample size (N - 10). Without this, AR(0) would use 10 more observations 
# than AR(10), making their AIC scores mathematically incomparable.
print(f"Comparing AIC values for different AR(p) models (Fixed Sample: N-{max_lag_test}):")
for p in range(0, max_lag_test + 1):
    # Fit the temporary model for the current lag 'p' (p=0 is White Noise)
    model_temp = AutoReg(df['sq_return'], lags=p, hold_back=max_lag_test).fit()
    # Print the calculated Akaike Information Criterion
    print(f"AR({p:02d}) -> AIC: {model_temp.aic:.4f}")

# Automatically select the optimal lag order for SQUARED returns using the AIC
selector_sq = ar_select_order(df['sq_return'], maxlag=max_lag_test, ic='aic')
# Extract the list of selected lags
selected_lags_sq = selector_sq.ar_lags
# Determine the highest lag order (p), handling the NoneType if 0 lags are selected
p_sq = max(selected_lags_sq) if selected_lags_sq is not None else 0

print(f"\nOptimal AR order for Squared Returns (Minimum AIC): AR({p_sq})")

# Fit an AR(p) model to the SQUARED returns using the optimal lags selected by AIC
if p_sq > 0:
    model_sq = AutoReg(df['sq_return'], lags=selected_lags_sq).fit()
    # Print a header indicating the start of the model summary
    print(f"\n--- ESTIMATION: AR({p_sq}) ON SQUARED RETURNS ---")
    # Print the statistical summary of the fitted model
    print(model_sq.summary())
else:
    print("\n--- ESTIMATION: WHITE NOISE ---")
    print("AIC selected 0 lags for the squared returns.")

"""
DISCUSSION (Squared Returns & Model Selection):
- Fixed Sample: To ensure a valid "apples-to-apples" comparison, Information Criteria 
  (like AIC or BIC) must be computed over the exact same estimation sample. 
- AIC Minimization: Inspecting the manual table, the AIC value drops as we add lags, 
  hitting its absolute mathematical minimum exactly at AR(3), and then starts to rise 
  again as the penalty for complexity outweighs the improved fit.
- Implication: Variance is predictable. Knowing past volatility gives us significant 
  statistical power to predict current volatility.
"""

# %% 3. MODELING STANDARD RETURNS (PRICE DYNAMICS)

# Print a separator line for the standard returns modeling section
print("\n" + "="*50)
# Print the title for the standard returns analysis
print("3. AR(p) MODEL FOR STANDARD RETURNS")
# Print the closing separator line
print("="*50)

# Automatically select the optimal lag order for STANDARD returns using the AIC
selector_ret = ar_select_order(df['return'], maxlag=max_lag_test, ic='aic')
# Extract the list of selected lags
selected_lags_ret = selector_ret.ar_lags
# Determine the highest lag order (p), handling the NoneType if 0 lags are selected
p_ret = max(selected_lags_ret) if selected_lags_ret is not None else 0

print(f"Optimal AR order for Standard Returns (AIC): AR({p_ret})")

# Fit the AR(p) model to the STANDARD returns
if p_ret > 0:
    model_ret = AutoReg(df['return'], lags=selected_lags_ret).fit()
    print(f"\n--- ESTIMATION: AR({p_ret}) ON STANDARD RETURNS ---")
    print(model_ret.summary())
else:
    print("\n--- ESTIMATION: WHITE NOISE ---")
    print("AIC selected 0 lags. The standard returns behave as a pure White Noise process.")

"""
DISCUSSION (Comparative Analysis):
- Standard Returns: For the standard returns, the AIC typically selects 0 lags (or 
  perhaps 1 lag that is statistically insignificant). The past sequence of returns 
  contains no useful information to predict tomorrow's return direction.
- Conclusion: We observe an efficient market in the 'first moment' (mean): prices 
  follow a random walk and you cannot consistently predict if tomorrow will be up or down. 
  However, the market is highly inefficient in the 'second moment' (variance): 
  volatility is extremely persistent and predictable. You cannot predict the direction, 
  but you can predict the intensity of the movement!
"""