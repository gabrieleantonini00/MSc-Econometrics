plt.title('DAX Daily Returns (2022)')
# Enable a light grid
plt.grid(True, alpha=0.3)

# Create the bottom subplot for the squared returns
plt.subplot(2, 1, 2)
# Plot the squared daily returns in red
plt.plot(df.index, df['sq_return'], color='firebrick')
# Set the title for the second subplot
plt.title('DAX Squared Daily Returns (Volatility Proxy)')
# Enable a light grid
plt.grid(True, alpha=0.3)

# Automatically adjust the padding between subplots
plt.tight_layout()
